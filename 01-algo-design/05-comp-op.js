// JS-08 - comparison operators 

/*
operadores relacionales o de comparación 

> Mayor que
a > b
a es mayor que b

< Menor que
a < b
a es menor que b

>= Mayor o igual que 
a >= b
a es mayor o igual que b

<= Menor o igual que
a <= b
a es menor o igual que b

Útil para booleans

== Igual 
a == b
a es igual a b 

Un solo = no es para igualdad, es para asignación

!= Distinto 
a != b
a es diferente de b

Operador estricto 
===

Operador diferente estricto
!==

*/

//el resultado de una operación relacional nos devuelve un boolean
let n01 = 22;
let n02 = 15;

console.log(n01 > n02);
console.log(n01 < n02);
console.log(n01 >= n02);
console.log(n01 <= n02);
console.log(n01 == n02);
console.log(n01 != n02);

console.log("22 es mayor que 15: " + (n01 > n02));